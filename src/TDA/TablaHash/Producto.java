public class Producto {
    
    String codigo;
    String nombre;
    String marca;
    int precio;
    String ruta;
    
    public Producto(String codigo_, String nombre_, String marca, int precio, String ruta){
        this.codigo=codigo_;
        this.nombre=nombre_;
        this.marca=marca;
        this.precio=precio;
        this.ruta=ruta;
    }
    
}
